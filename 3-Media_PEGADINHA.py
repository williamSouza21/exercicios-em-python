valor1 = float(input("Digite o 1° valor: "))
valor2 = float(input("Digite o 2° valor: "))

média = (valor1 + valor2)/2
# Python aceita variáveis escritas com caracteres especiais.
print("A média entre {:.1f} e {:.1f} é {:.1f}".format(valor1, valor2, média))

# OBS: Não esquecer dos parênteses antes de dividir por 2!
